        <h3 id="myc">Administre las cookies</h3>
        <p>Puede especificar cómo se utilizan las cookies configurando sus preferencias al respecto.</p>
        <h4>Chrome</h4>
        <ol>
        <li>Haga clic en el menú de Chrome en la barra de herramientas del navegador.</li>
        <li>Seleccione <strong>Configuración</strong>.</li>
        <li>Haga clic en <strong>Mostrar opciones avanzadas</strong>.</li>
        <li>En la sección <strong>Privacidad</strong>, haga clic en <strong>Configuración de contenido</strong>.</li>
        <li>En la sección <strong>Cookies</strong>, seleccione uno de estos parámetros: <br>
        Eliminar cookies<br>
        Bloquear cookies de forma predeterminada<br>
        Permitir cookies de forma predeterminada<br>
        Guardar cookies y datos de sitios de forma predeterminada hasta cerrar el navegador<br>
        Incluir excepciones de cookies de dominios o sitios web específicos</li>
        </ol>
        <h4>Mozilla Firefox</h4>
        <ol>
        <li>Abra Firefox. En el menú <strong>Editar</strong>, seleccione <strong>Preferencias</strong>.</li>
        <li>En <strong>Privacidad y Seguridad</strong>, seleccione <strong>Cookies</strong>.</li>
        <li>Puede seleccionar los parámetros siguientes:<br>
        Desactivar cookies<br>
        Activar cookies solo para el sitio Web de origen<br>
        Activar todas las cookies <br>
        Advertirme antes de almacenar cookie</li>
        </ol>
        <h4>Internet Explorer 9 o 10</h4>
